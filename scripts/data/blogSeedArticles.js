function escapeHtml(value = '') {
  return String(value)
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
    .replaceAll('"', '&quot;')
    .replaceAll("'", '&#39;')
}

function slugify(value = '') {
  return value
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
}

function buildCoverImage(slug) {
  return `/mock-covers/${slug}.svg`
}

function buildContent(article) {
  const facts = article.facts
    .map((fact) => `<li>${escapeHtml(fact)}</li>`)
    .join('')

  const takeaways = (article.takeaways || [])
    .map((item) => `<li>${escapeHtml(item)}</li>`)
    .join('')

  return `
    <p>${escapeHtml(article.intro)}</p>
    <h2>Cosa racconta la fonte</h2>
    <ul>${facts}</ul>
    <h2>Lettura da blog</h2>
    <p>${escapeHtml(article.blogAngle)}</p>
    <h2>Cosa tenere d'occhio</h2>
    <ul>${takeaways}</ul>
  `.trim()
}

function makeArticle(article) {
  const slug = article.slug || slugify(article.title)
  return {
    ...article,
    slug,
    excerpt: article.excerpt.trim(),
    content: buildContent(article),
    cover_image: article.cover_image || buildCoverImage(slug),
    status: 'published',
  }
}

